package com.rednikeeg;

public class LAMath {
    public static BigNumber minus(BigNumber num1, BigNumber num2) {
        return num1.minus(num2);
    }

    public static BigNumber plus(BigNumber num1, BigNumber num2) {
        return num1.plus(num2);
    }

    public static BigNumber multiply(BigNumber num1, BigNumber num2) {
        return num1.multiply(num2);
    }

    public static BigNumber divide(BigNumber num1, BigNumber num2) {
        return num1.divide(num2);
    }
}
